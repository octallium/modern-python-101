# ----------------------------------------------------
# String Formatting for getting desired string outputs
# ----------------------------------------------------

name = 'Louis'
age = 13
dog = "Chiko"
cat = "Koko"

print(f"Hello {name}!")
print(f"{name} is {age} years old.")
print(f"{name} has a dog named {dog}.")
print(f"{name} has a cat named {cat}.")


print(f"hello {name}!".capitalize())
print(f"{name} is {age} years old.".title())
print(f"{name} has a dog named {dog}.".upper())
print(f"{name} has a cat named {cat}.".lower())
